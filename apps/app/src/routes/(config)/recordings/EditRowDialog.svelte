<script lang="ts">
	import WhisperingTooltip from '$lib/components/WhisperingTooltip.svelte';
	import { PencilIcon as EditIcon, Loader2Icon } from '$lib/components/icons';
	import { Button } from '$lib/components/ui/button';
	import * as Dialog from '$lib/components/ui/dialog';
	import { Input } from '$lib/components/ui/input';
	import { Label } from '$lib/components/ui/label';
	import { Textarea } from '$lib/components/ui/textarea';
	import type { Recording } from '$lib/services/RecordingDbService';
	import { renderErrorAsToast } from '$lib/services/errors';
	import { recordings } from '$lib/stores';
	import { Effect } from 'effect';

	let { recording }: { recording: Recording } = $props();

	let isDialogOpen = $state(false);
	let isDeleting = $state(false);
	let isSaving = $state(false);
</script>

<Dialog.Root bind:open={isDialogOpen}>
	<Dialog.Trigger>
		<WhisperingTooltip tooltipText="Edit recording" variant="ghost" size="icon">
			<EditIcon class="h-4 w-4" />
		</WhisperingTooltip>
	</Dialog.Trigger>
	<Dialog.Content class="sm:max-w-[425px]">
		<Dialog.Header>
			<Dialog.Title>Edit recording</Dialog.Title>
			<Dialog.Description>
				Make changes to your recording here. Click save when you're done.
			</Dialog.Description>
		</Dialog.Header>
		<form
			class="grid gap-4 py-4"
			onsubmit={async (e) => {
				e.preventDefault();
				isSaving = true;
				await recordings.updateRecording(recording);
				isSaving = false;
				isDialogOpen = false;
			}}
		>
			<div class="grid grid-cols-4 items-center gap-4">
				<Label for="title" class="text-right">Title</Label>
				<Input id="title" bind:value={recording.title} class="col-span-3" />
			</div>
			<div class="grid grid-cols-4 items-center gap-4">
				<Label for="subtitle" class="text-right">Subtitle</Label>
				<Input id="subtitle" bind:value={recording.subtitle} class="col-span-3" />
			</div>
			<div class="grid grid-cols-4 items-center gap-4">
				<Label for="timestamp" class="text-right">Timestamp</Label>
				<Input id="timestamp" bind:value={recording.timestamp} class="col-span-3" />
			</div>
			<div class="grid grid-cols-4 items-center gap-4">
				<Label for="transcribedText" class="text-right">Transcribed Text</Label>
				<Textarea id="transcribedText" bind:value={recording.transcribedText} class="col-span-3" />
			</div>
			<div class="grid grid-cols-4 items-center gap-4">
				<Label for="blob" class="text-right">Blob</Label>
				<audio
					src={recording.blob ? URL.createObjectURL(recording.blob) : ''}
					controls
					class="col-span-3 mt-2 h-8 w-full"
				></audio>
			</div>
			<Dialog.Footer>
				<Button
					class="mr-auto"
					on:click={async () => {
						isDeleting = true;
						await recordings.deleteRecordingById(recording.id);
						isDeleting = false;
						isDialogOpen = false;
					}}
					variant="destructive"
					disabled={isDeleting}
				>
					{#if isDeleting}
						<Loader2Icon class="mr-2 h-4 w-4 animate-spin" />
					{/if}
					Delete
				</Button>
				<Button on:click={() => (isDialogOpen = false)} variant="secondary">Cancel</Button>
				<Button type="submit" disabled={isSaving}>
					{#if isSaving}
						<Loader2Icon class="mr-2 h-4 w-4 animate-spin" />
					{/if}
					Save
				</Button>
			</Dialog.Footer>
		</form>
	</Dialog.Content>
</Dialog.Root>
